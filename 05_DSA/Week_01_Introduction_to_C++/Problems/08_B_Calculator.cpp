#include <bits/stdc++.h>
using namespace std;

int main() {
    
    long long n;
    long long m;
    
    cin >> n;
    cin >> m;

    cout << n << " + " << m << " = "<< n+m << endl;
    cout << endl;
    cout << n << " - " << m << " = "<< n-m << endl;
    cout << endl;
    cout << n << " * " << m << " = "<< n*m << endl;
    cout << endl;
    cout << n << " / " << m << " = "<< n/m << endl;
    cout << endl;
    cout << n << " % " << m << " = "<< n%m << endl;
    cout << endl;
   
   
}
